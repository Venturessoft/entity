package com.vsm.lib.dto.seldb;

public class HuCatMonedaDto {
	private long numCia;
	private String claveMoneda;
	private String abreviacion;
	private String descripcion;
	private String monedaCorriente;
	private String simbolo;
	private String status;
	/**
	 * @return the numCia
	 */
	public long getNumCia() {
		return numCia;
	}
	/**
	 * @param numCia the numCia to set
	 */
	public void setNumCia(long numCia) {
		this.numCia = numCia;
	}
	/**
	 * @return the claveMoneda
	 */
	public String getClaveMoneda() {
		return claveMoneda;
	}
	/**
	 * @param claveMoneda the claveMoneda to set
	 */
	public void setClaveMoneda(String claveMoneda) {
		this.claveMoneda = claveMoneda;
	}
	/**
	 * @return the abreviacion
	 */
	public String getAbreviacion() {
		return abreviacion;
	}
	/**
	 * @param abreviacion the abreviacion to set
	 */
	public void setAbreviacion(String abreviacion) {
		this.abreviacion = abreviacion;
	}
	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}
	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	/**
	 * @return the monedaCorriente
	 */
	public String getMonedaCorriente() {
		return monedaCorriente;
	}
	/**
	 * @param monedaCorriente the monedaCorriente to set
	 */
	public void setMonedaCorriente(String monedaCorriente) {
		this.monedaCorriente = monedaCorriente;
	}
	/**
	 * @return the simbolo
	 */
	public String getSimbolo() {
		return simbolo;
	}
	/**
	 * @param simbolo the simbolo to set
	 */
	public void setSimbolo(String simbolo) {
		this.simbolo = simbolo;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
